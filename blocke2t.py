class ExperienceBuffer:

    def __init__(self):

        self.clear()

    def clear(self):

        self.states = []

        self.actions = []

        self.rewards = []

        self.next_states = []

        self.dones = []

class BlockE2TMORLAgent:

    def __init__(self):

        self.actor = ActorNetwork().to(DEVICE)

        self.critic = CriticNetwork().to(DEVICE)

        self.actor_optimizer = optim.Adam(
            self.actor.parameters(),
            lr=ACTOR_LR
        )

        self.critic_optimizer = optim.Adam(
            self.critic.parameters(),
            lr=CRITIC_LR
        )

        self.memory = ExperienceBuffer()

    def select_action(self,state):

        state = state.unsqueeze(0).to(DEVICE)

        probs = self.actor(state)

        distribution = torch.distributions.Categorical(
            probs
        )

        action = distribution.sample()

        log_prob = distribution.log_prob(
            action
        )

        return action.item(), log_prob

    def store_transition(
        self,
        state,
        action,
        reward,
        next_state,
        done
    ):

        self.memory.states.append(state)

        self.memory.actions.append(action)

        self.memory.rewards.append(reward)

        self.memory.next_states.append(next_state)

        self.memory.dones.append(done)

    def compute_advantages(self):

        returns = []

        discounted_reward = 0

        for reward, done in zip(

            reversed(self.memory.rewards),

            reversed(self.memory.dones)

        ):

            if done:

                discounted_reward = 0

            discounted_reward = (
                reward
                +
                GAMMA * discounted_reward
            )

            returns.insert(
                0,
                discounted_reward
            )

        returns = torch.FloatTensor(
            returns
        ).to(DEVICE)

        states = torch.stack(
            self.memory.states
        ).to(DEVICE)

        values = self.critic(
            states
        ).squeeze()

        advantages = (
            returns
            -
            values.detach()
        )

        return returns, advantages

    def update(self, trust_factor=1.0):

        returns, advantages = \
            self.compute_advantages()

        states = torch.stack(
            self.memory.states
        ).to(DEVICE)

        actions = torch.LongTensor(
            self.memory.actions
        ).to(DEVICE)

        probs = self.actor(states)

        distribution = torch.distributions.Categorical(
            probs
        )

        log_probs = distribution.log_prob(
            actions
        )

        actor_loss = -(
            log_probs
            *
            advantages
            *
            trust_factor
        ).mean()

        values = self.critic(
            states
        ).squeeze()

        critic_loss = F.mse_loss(
            values,
            returns
        )

        self.actor_optimizer.zero_grad()

        actor_loss.backward()

        self.actor_optimizer.step()

        self.critic_optimizer.zero_grad()

        critic_loss.backward()

        self.critic_optimizer.step()

        self.memory.clear()

        return (
            actor_loss.item(),
            critic_loss.item()
        )

