class DeterministicGridGenerator:

    def __init__(self, size):

        self.size = size

    def generate(self):

        grid = np.zeros((self.size, self.size))

        obstacle_locations = [

            (2,2),(2,3),(2,4),

            (4,5),(5,5),(6,5),

            (7,2),(7,3),

            (1,7),(2,7),

            (5,8),(6,8)

        ]

        for r,c in obstacle_locations:

            grid[r,c] = -1

        return grid

class TrustBlock:

    def __init__(self,
                 agent_id,
                 trust_score,
                 validation_score,
                 previous_hash):

        self.agent_id = agent_id

        self.trust_score = trust_score

        self.validation_score = validation_score

        self.previous_hash = previous_hash

        self.current_hash = self.compute_hash()

    def compute_hash(self):

        record = f"{self.agent_id}" \
                 f"{self.trust_score}" \
                 f"{self.validation_score}" \
                 f"{self.previous_hash}"

        return hashlib.sha256(
            record.encode()
        ).hexdigest()

class TrustLedger:

    def __init__(self):

        self.chain = []

    def add_record(self,
                   agent_id,
                   trust_score,
                   validation_score):

        prev_hash = "GENESIS"

        if len(self.chain) > 0:

            prev_hash = self.chain[-1].current_hash

        block = TrustBlock(
            agent_id,
            trust_score,
            validation_score,
            prev_hash
        )

        self.chain.append(block)

    def size(self):

        return len(self.chain)

class TrustManager:

    def __init__(self):

        self.trust_scores = {}

    def initialize_agent(self, agent_id):

        self.trust_scores[agent_id] = INITIAL_TRUST

    def update_trust(self,
                     agent_id,
                     validation_score):

        old = self.trust_scores[agent_id]

        new_score = (
            TRUST_ALPHA * old
            +
            (1 - TRUST_ALPHA) *
            validation_score
        )

        self.trust_scores[agent_id] = new_score

        return new_score

    def get_trust(self, agent_id):

        return self.trust_scores[agent_id]

