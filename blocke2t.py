class CloudGridEnvironment:

    def __init__(self):

        self.grid_size = GRID_SIZE

        self.grid = DeterministicGridGenerator(
            GRID_SIZE
        ).generate()

        self.start = (0, 0)

        self.goal = (9, 9)

        self.security_zones = self.create_security_zones()

        self.latency_map = self.create_latency_map()

    # ------------------------------------------------------

    def reset(self):

        return self.start

    # ------------------------------------------------------

    def inside_grid(self, x, y):

        return (
            0 <= x < self.grid_size
            and
            0 <= y < self.grid_size
        )

    # ------------------------------------------------------

    def is_obstacle(self, x, y):

        return self.grid[x, y] == -1

    # ------------------------------------------------------

    def create_security_zones(self):

        return {

            (3,3):0.4,

            (3,4):0.4,

            (6,6):0.5,

            (6,7):0.5,

            (8,3):0.6

        }

    # ------------------------------------------------------

    def create_latency_map(self):

        latency = np.zeros(
            (GRID_SIZE, GRID_SIZE)
        )

        for i in range(GRID_SIZE):

            for j in range(GRID_SIZE):

                latency[i, j] = (

                    5

                    + 0.5 * i

                    + 0.3 * j

                )

        return latency

    # ------------------------------------------------------

    def energy_cost(
        self,
        current,
        nxt
    ):

        distance = (

            abs(current[0] - nxt[0])

            +

            abs(current[1] - nxt[1])

        )

        return 1.5 * distance

    # ------------------------------------------------------

    def security_score(
        self,
        position
    ):

        if position in self.security_zones:

            return self.security_zones[position]

        return 1.0

    # ------------------------------------------------------

    def step(
        self,
        agent,
        action
    ):

        dx, dy = ACTION_MAP[action]

        x, y = agent.position

        nx = x + dx

        ny = y + dy

        # Boundary

        if not self.inside_grid(nx, ny):

            return (

                agent.position,

                -25,

                False,

                {

                    "collision": True,

                    "energy": 0,

                    "latency": 0,

                    "security": 0

                }

            )

        # Obstacle

        if self.is_obstacle(nx, ny):

            return (

                agent.position,

                -25,

                False,

                {

                    "collision": True,

                    "energy": 0,

                    "latency": 0,

                    "security": 0

                }

            )

        next_position = (nx, ny)

        energy_used = self.energy_cost(

            agent.position,

            next_position

        )

        latency = self.latency_map[nx, ny]

        security = self.security_score(

            next_position

        )

        agent.position = next_position

        agent.energy -= energy_used

        agent.latency += latency

        done = False

        reward = -1

        if next_position == self.goal:

            done = True

            reward = 100

        info = {

            "energy": energy_used,

            "latency": latency,

            "security": security,

            "collision": False

        }

        return (

            next_position,

            reward,

            done,

            info

        )

