class ValidationEngine:

    def validate(

        self,

        local_actor,

        reference_actors

    ):

        similarities = []

        local_params = \
            get_model_parameters(
                local_actor
            )

        for ref in reference_actors:

            ref_params = \
                get_model_parameters(
                    ref
                )

            score = 0

            count = 0

            for key in local_params:

                l = local_params[key].flatten()

                r = ref_params[key].flatten()

                cosine = F.cosine_similarity(

                    l.unsqueeze(0),

                    r.unsqueeze(0)

                ).item()

                score += cosine

                count += 1

            similarities.append(
                score/count
            )

        return np.mean(
            similarities
        )

