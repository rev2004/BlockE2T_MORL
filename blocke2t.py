class MORLReward:

    def compute(self,
                energy,
                latency,
                security,
                trust,
                reached_goal):

        energy_term = 1/(1+energy)

        latency_term = 1/(1+latency)

        security_term = security

        reward = (

            W_ENERGY
            *
            energy_term

            +

            W_LATENCY
            *
            latency_term

            +

            W_SECURITY
            *
            security_term

        )

        reward = reward * trust

        if reached_goal:

            reward += 100

        return reward

