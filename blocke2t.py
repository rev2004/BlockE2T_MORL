def step(self, agent, action):

    dx, dy = ACTION_MAP[action]

    x, y = agent.position

    nx = x + dx
    ny = y + dy

    # Boundary check
    if not self.inside_grid(nx, ny):

        return (
            agent.position,
            -25,
            False,
            {
                "collision": True,
                "energy": 0,
                "latency": 0,
                "security": 0
            }
        )

    # Obstacle check
    if self.is_obstacle(nx, ny):

        return (
            agent.position,
            -25,
            False,
            {
                "collision": True,
                "energy": 0,
                "latency": 0,
                "security": 0
            }
        )

    next_position = (nx, ny)

    energy_used = self.energy_cost(
        agent.position,
        next_position
    )

    latency = self.latency_map[nx, ny]

    security = self.security_score(
        next_position
    )

    # Update agent state
    agent.position = next_position

    agent.energy -= energy_used

    agent.latency += latency

    done = False

    reward = -1

    if next_position == self.goal:

        done = True

        reward = 100

    info = {

        "energy": energy_used,

        "latency": latency,

        "security": security,

        "collision": False

    }

    return (
        next_position,
        reward,
        done,
        info
    )

