class ActorNetwork(nn.Module):

    def __init__(self,
                 state_dim=5,
                 action_dim=4):

        super().__init__()

        self.fc1 = nn.Linear(
            state_dim,
            128
        )

        self.fc2 = nn.Linear(
            128,
            64
        )

        self.fc3 = nn.Linear(
            64,
            action_dim
        )

    def forward(self,x):

        x = F.relu(self.fc1(x))

        x = F.relu(self.fc2(x))

        logits = self.fc3(x)

        probs = F.softmax(
            logits,
            dim=-1
        )

        return probs

