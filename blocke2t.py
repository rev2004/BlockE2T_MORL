def compute_update(

    global_model,

    local_model

):

    delta = {}

    global_dict = global_model.state_dict()

    local_dict = local_model.state_dict()

    for key in global_dict.keys():

        delta[key] = (

            local_dict[key]

            -

            global_dict[key]

        )

    return delta

